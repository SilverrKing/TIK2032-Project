                <h3>Champions League:</h3>
                <p>
                    Liga Champions UEFA (disingkat UCL, Inggris: UEFA Champions League) atau Liga Champions Eropa (LCE) yang sebelumnya bernama Piala Eropa (European Cup) adalah sebuah kompetisi sepak bola antarklub oleh UEFA dan diikuti oleh klub dari liga atau divisi tertinggi Eropa. Kompetisi ini merupakan salah satu turnamen paling bergengsi di dunia dan kompetisi antarklub paling bergengsi di sepak bola Eropa, yang hanya diikuti oleh juara liga nasional (dan juga juara kedua untuk beberapa negara) dari setiap asosiasi nasional anggota UEFA.
                </p>
                <h4>Pemenang Champions League</h4>
                <p>
                    Hingga musim 2023/2024, 68 edisi Liga Champions UEFA telah diadakan dan total 140 klub telah berkompetisi. Trofi tersebut telah dimenangkan oleh 23 klub, dengan Real Madrid menjadi klub paling sukses dengan 14 gelar juara. Klub lain yang meraih banyak gelar juara antara lain AC Milan (7), Liverpool (6), Bayern Munich (6), Barcelona (5), Ajax (4), Inter Milan (3), Manchester United (3), Chelsea (2), Benfica (2), Nottingham Forest (2), Juventus (2), Porto (2), Borussia Dortmund (1), Celtic (1), Feyenoord (1), Hamburg (1), PSV Eindhoven (1), Red Star Belgrade (1), Steaua Bucharest (1), Marseille (1), Olympique Lyonnais (1), Bayern Leverkusen (1), Valencia (1).
                </p>
                <h4>Info Seputar Champions League</h4>
                <p>
                    Diperkenalkan sejak 1992, kompetisi ini menggantikan Piala Champions Eropa atau disebut sebagai Piala Eropa, yang telah bergulir sejak 1955, dengan menambahkan babak penyisihan grup ke dalam kompetisi dan memungkinkan masuknya beberapa klub dari beberapa negara tertentu. Real Madrid merupakan klub tersukses sepanjang sejarah kompetisi ini, dengan empat belas kali meraih gelar juara, termasuk gelar juara berturut-turut pada sepuluh edisi awal kompetisi ini. Manchester City adalah juara bertahan setelah mengalahkan Inter Milan dengan skor 1–0 pada pertandingan final Liga Champions UEFA 2023.
                </p>
