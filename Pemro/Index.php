                <h3>Piala Dunia:</h3>
                <p>
                    FIFA World Cup atau sering disebut Piala Dunia , adalah kompetisi sepak bola internasional yang diikuti oleh tim nasional senior putra anggota Federasi Sepak Bola Internasional (Fédération Internationale de Football Association, FIFA), badan pengatur sepak bola dunia.
                    Piala Dunia adalah turnamen sepakbola paling bergengsi di dunia, serta acara olahraga tunggal yang paling banyak ditonton dan diikuti di dunia.
                </p>
                <h4>Pemenang Piala Dunia</h4>
                <p>
                    Hingga Piala Dunia FIFA 2022, 22 turnamen telah diadakan dan total 80 tim nasional telah berkompetisi. Trofi tersebut telah dimenangkan oleh delapan tim nasional. Brasil, dengan torehan lima gelar juara, adalah satu-satunya tim yang bermain di setiap edisi turnamen. Pemenang Piala Dunia lainnya adalah Jerman dan Italia, dengan masing-masing empat gelar; Argentina, dengan tiga gelar; Prancis dan Uruguay, masing-masing dua gelar; serta Inggris dan Spanyol, masing-masing dengan satu gelar.
                </p>
                <h4>Info Seputar Piala Dunia</h4>
                <p>
                    Kejuaraan ini telah diselenggarakan setiap empat tahun sekali sejak turnamen 1930, kecuali pada tahun 1942 dan 1946, yang tidak diselenggarakan karena Perang Dunia II. Juara bertahan Piala Dunia saat ini adalah Argentina, yang menjuarai turnamen 2022 di Qatar. Format turnamen ini melibatkan babak kualifikasi, yang berlangsung selama kurang lebih tiga tahun sebelum putaran final dimulai, untuk menentukan tim mana yang lolos ke putaran final. Pada putaran final, sebanyak 32 tim bersaing memperebutkan gelar di tempat-tempat di negara tuan rumah selama sekitar satu bulan. Negara tuan rumah secara otomatis lolos ke babak grup putaran final. Piala Dunia FIFA telah dijadwalkan untuk diperbanyak menjadi 48 tim mulai edisi 2026.
            </article>
